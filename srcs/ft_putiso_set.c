#include "ft_printf.h"

int	iso_set(char *ret, int itr, char input)
{
	if (input == ',')
	{
		ret[itr++] = ',';
		ret[itr++] = ' ';
	}
	if (input == 'b')
	{
		ret[itr++] = ' ';
		ret[itr++] = 'B';
		ret[itr++] = 'C';
	}
	return (itr);
}
